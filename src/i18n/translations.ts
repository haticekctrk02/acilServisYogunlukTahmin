type NestedKeyOf<T, P extends string = ''> = T extends object
  ? {
      [K in keyof T]: K extends string
        ? T[K] extends object
          ? NestedKeyOf<T[K], P extends '' ? K : `${P}.${K}`>
          : P extends ''
            ? K
            : `${P}.${K}`
        : never
    }[keyof T]
  : never

